public class StringExercise {
    public static void main(String[] args) {
        //1. String Basics exercises

        String verse = "We don't need no education \nWe don't need no thought control";
        System.out.println(verse);

        String escape = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(escape);

        String cDrive = "In windows, the main drive is usually C: \\";
        System.out.println(cDrive);

        String lottaBackslashing = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(lottaBackslashing);

    }
}
